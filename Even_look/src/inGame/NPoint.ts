class NPoint {
    public x: number;
    public y: number;

    public constructor(arg1: number = 0, arg2: number = 0) {
        this.x = arg1;
        this.y = arg2;
        return;
    }

    public setTo(arg1: number, arg2: number): void {
        this.x = arg1;
        this.y = arg2;
        return;
    }
}

